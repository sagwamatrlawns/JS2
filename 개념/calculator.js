const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },

  minus: function (a, b) {
    console.log(a - b);
  },

  multi: function (a, b) {
    console.log(a * b);
  },

  div: function (a, b) {
    console.log(a / b);
  },

  power: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(5, 2);
calculator.minus(5, 2);
calculator.multi(5, 2);
calculator.div(5, 2);
calculator.power(5, 2);
