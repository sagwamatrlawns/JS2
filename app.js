// Variables (변수))
// JS에서는 변수명 사이에 공백을 넣을 수 없다.
// 공백대신 다음 글자를 대문자로 입력해서 구분하는 것이 일반적이다.
// 기본적으로 const를 쓰고 필요할 떄만 let을 쓰고, var은 쓰지 말자.

const a = 5;
const b = 2;
let myName = "KJ";

console.log(a + b);
console.log(a * b);
console.log(a / b);
console.log("hello " + myName);

myName = "Kim Jun";

console.log("your new name is " + myName);
